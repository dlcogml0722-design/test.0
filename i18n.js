let currentLang = 'ko';

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('pilgrim_lang', lang);
  document.documentElement.lang = lang;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (I18N[lang] && I18N[lang][key]) {
      el.textContent = I18N[lang][key];
    }
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (I18N[lang] && I18N[lang][key]) {
      el.placeholder = I18N[lang][key];
    }
  });
  // Update lang switcher active state
  document.querySelectorAll('.lang-switcher__btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
}

function t(key, params = {}) {
  let str = I18N[currentLang]?.[key] || I18N['ko']?.[key] || key;
  Object.entries(params).forEach(([k, v]) => {
    str = str.replace(`\${${k}}`, v);
  });
  return str;
}

function getLocalizedField(obj) {
  if (!obj) return '';
  if (typeof obj === 'string') return obj;
  return obj[currentLang] || obj['ko'] || obj['en'] || '';
}

function initI18n() {
  const saved = localStorage.getItem('pilgrim_lang');
  const browserLang = navigator.language?.slice(0, 2);
  const lang = saved || (I18N[browserLang] ? browserLang : 'ko');
  setLang(lang);
}
